void main (){
  var frist = 10;
  var secound = 3;

  var pertambahan = frist + secound;
  var pengurangan = frist - secound;
  var pembagian = frist / secound;
  var perkalian = frist * secound;
  var modulus = frist % secound;

  print("hasil dari ${frist} + $secound adalah ${pertambahan}");
  print("hasil dari ${frist} - $secound adalah ${pengurangan}");
  print("hasil dari ${frist} / $secound adalah ${pembagian}");
  print("hasil dari ${frist} * $secound adalah ${perkalian}");
  print("hasil dari ${frist} % $secound adalah ${modulus}");
}